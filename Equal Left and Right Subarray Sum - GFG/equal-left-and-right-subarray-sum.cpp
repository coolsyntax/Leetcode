class Solution {
  public:
    int equalSum(int N, vector<int> &A) {
        // code here
        if(N == 1){
            return 1;
        }
        int arr_sum = accumulate(A.begin(), A.end(), 0);
        int current_sum = 0;
        arr_sum -= A[0];
        for(int i = 1; i <= N; i++){
            current_sum += A[i-1];
            arr_sum -= A[i];
            if(current_sum == arr_sum){
                return i+1;
                break;
            }
        }
        return -1;
    }
};


